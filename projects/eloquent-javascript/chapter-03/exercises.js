//min takes two arguments and returns their minimum
function min(a, b) {
  
  //returning the lesser of a or b
  return (a < b ? a : b);
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  // changes negative numbers to positive
  if(num < 0) num *= -1;
  
  // returns true if num is 0
  if(num === 0) return true;
  
  //returns false if num is 1
  else if(num === 1) return false;
  
  // calls the function again until num === 0 or 1
  else return isEven(num - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  
  //char counter
  let counter = 0;
  
  //iterating over a string
  for(let i = 0; i < string.length; i++){
    
    //if element in string === char, increase counter by 1
    if(string[i] === char){
      counter++;
    }
    
  }
  //return number of char's counted
  return counter;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  
  //'B' counter
  let counter = 0;
  
  //iterating over a string
  for(let i = 0; i < string.length; i++){
    
    //if element in string === 'B', increase counter by 1
    if(string[i] === 'B'){
      counter++;
    }
    
  }
  //return 'B' counter    
  return counter;

}


